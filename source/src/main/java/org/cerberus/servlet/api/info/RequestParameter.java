package org.cerberus.servlet.api.info;

/**
 * A request parameter that can be inserted into a {@link SinglePointHttpServletInfo}
 *
 * @author Aurelien Bourdon
 */
public class RequestParameter {

    private final String name;
    private final String description;
    private final boolean required;

    public RequestParameter(final String name, final String description) {
        this.name = name;
        this.description = description;
        required = true;
    }

    public RequestParameter(final String name, final String description, final boolean required) {
        this.name = name;
        this.description = description;
        this.required = required;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public boolean isRequired() {
        return required;
    }

    @Override
    public boolean equals(final Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        final RequestParameter that = (RequestParameter) o;

        return name != null ? name.equals(that.name) : that.name == null;
    }

    @Override
    public int hashCode() {
        return name != null ? name.hashCode() : 0;
    }

}
